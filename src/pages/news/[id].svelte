          Namun sejatinya, menikmati kopi yang sebenarnya, adalah menikmati kopi
          murni, bukan dengan bahan campuran dan rasa bermacam-macam. Paling
          tidak, konsep itu yang diterapkan Wahyudi, pemilik Wakoka (warung kopi
          kawula alit). Sebuah kedai kopi di Jalan Menur, Kabupaten Ponorogo,
          Jawa Timur. Wahyudi ingin menghadirkan rasa kopi asli tanpa ada bahan
          tambahan lain.
          <br />
          <br />
          Pada zaman dahulu, yang dimaksud gula adalah tebu dan gula aren. “Gula
          arena atau gula dari kelapa, jelas harganya lebih mahal dibanding
          dengan gula tebu. Itulah sebabnya, gula aren hanya bisa dibeli dari
          kalangan priyayi dan pejabat kerajaan. Sedangkan rakyat jelata atau
          kawula alit, hanya kuat membeli gula tebu,” ujar Wahyudi, pria yang
          sering mengenakan kopyah dari koran ini.
          <br />
          <br />
          Di Wakoka, Wahyudi ingin kembali menghadirkan budaya minum kopi zaman
          lampau. Kopi pahit, dihidangkan dengan dua potong kecil gula aren dan
          gula tebu. “Kopi yang kami hidangkan juga tidak digiling halus,
          melainkan giling kasar,” tambahnya.
          <br />
          <br />
          Untuk menikmatinya, pengunjung menggigit gula, lalu diikuti dengan
          menyeruput kopi panas. “Nikmatnya kopi hanya pada saat masih panas.
          Seberapa besar gigitan gula, juga menentukan kadar manisnya kopi yang
          diminum,“ tandas Wahyudi, yang sudah bertahan 10 tahun menjaga konsep
          kopinya.
          <br />
          <br />
          Itulah keuntungan kopi cokot, bisa menentukan sendiri kadar manis
          kopi. Ini berbeda dengan kopi yang langsung diseduh dengan gula. Satu
          cangkir kopi, pahitnya atau manisnya kopi akan sama hingga habis.
          <br />
          <br />
          Meski begitu, Wakoka juga menyediakan kopi jenis lain, seperti kopi
          susu, dan kopi jahe. Dan disediakan juga, kopi yang bukan cokot. “Saya
          tetap mengakomodasi keinginan orang, karena mereka mempunyai cara
          tersendiri dalam menikmati kopi,” tambahnya.
          <br />
          <br />
          Di warungnya, pengunjung juga dimanjakan suasana tradisional. Sebuah
          rumah Joglo khas Jawa, menjadi bangunan utama warung miliknya.
          Mebelnya pun Wahyudi tidak menghadirkan yang modern.
          <br />
          <br />
          Ia malah menjadikan gerobog (brangkasnya orang Jawa) sebagai meja, dan
          lesung (tempat menumbuk padi zaman dahulu) sebagai kursi. “Tetapi di
          sini ada free wifi-nya,” ujarnya berseloroh.
          <br />
          <br />
          Selain kopi cokot, berbagai makanan tradisional juga bisa didapatkan.
          Seperti kacang rebus, ketela rebus, pisang goreng, dan sebagainya.
          “Sekali lagi, konsepnya tradisional,” tuturnya.
          <br />
          <br />
          Dalam sehari, pria itu bisa menjual 4-5 kilogram kopi. Kopi yang
          dimasak, dibeli dari pasar tradisional di Ponorogo. Tidak jarang ada
          orang yang memesan bubuk kopinya saja, untuk dibawa pulang.
          <br />
          <br />
          “Saya beberapa kali, mengantarkan kopi saya ke Gus Ipul (Saifullah
          Yusuf, Wakil Gubernur Jawa Timur). Beliau juga pernah ngopi di sini
          lima kali. Pak Karwo (Soekarwo, Gubernur Jawa Timur) juga pernah ke
          sini,” cerita Wahyudi bangga.
          <br />
          <br />
          Tidak hanya itu, beberapa artis pernah singgah ke Wakoka. Mereka
